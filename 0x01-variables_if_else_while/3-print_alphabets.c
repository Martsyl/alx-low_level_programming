#include <stdio.h>
/**
 * main - Entry point
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
	char lowertoUpper;

	for (lowertoUpper = 'a'; lowertoUpper <= 'z'; char)
		putchar(lowertoUpper);
	for (lowertoUpper = 'A'; lowertoUpper <= 'Z'; char)
		putchar(lowertoUpper);
	putchar('\n');
	return (0);
}
