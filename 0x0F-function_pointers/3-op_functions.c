#include "3-calc.h"
/**
 * op_add - addition
 * @a: integer.
 * @b: integer.
 *
 * Return: sum of int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtraction
 * @a: integer.
 * @b: integer.
 *
 * Return: sub of int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication
 * @a: integer.
 * @b: integer.
 *
 * Return: multiplication of int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_mod - modulus
 * @a: integer.
 * @b: integer.
 *
 * Return: modulus of int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
/**
 * op_div - check code
 * @a: integer.
 * @b: integer.
 *
 * Return: remainder of int
 */
int op_div(int a, int b)
{
	return (a / b);
}
